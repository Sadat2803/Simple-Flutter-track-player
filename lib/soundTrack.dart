class SoundTrack {
  String title;
  String author;
  String imagePath;
  String soundTrackUrl;
  SoundTrack(String title, String author, String imagePath, String soundTrackUrl)
  {
    this.title = title;
    this.author = author ;
    this.imagePath = imagePath ;
    this.soundTrackUrl = soundTrackUrl;
  }
}